package JavaProgramPractice.IntegerProgram;

public class Integer01_SwapTwoNumber {
    public static void main(String[] args) {

        int a = 100;
        int b = 200;

         a=a+b; //300
         b=a-b; //100
         a=a-b; //200

        System.out.println(a);
        System.out.println(b);
    }
}
